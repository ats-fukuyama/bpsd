C     $Id$
C
      IMPLICIT COMPLEX*16(C),REAL*8(A,B,D-F,H,O-Z)
      CHARACTER*32 KNAMEQ
C
      NRMAX=50
      NTHMAX=16
      NSUMAX=41
C
      CALL GSOPEN
    1 WRITE(6,*) '# INPUT 1:TASK/EQ  2:JFT2M'
      READ(5,*,ERR=1,END=9000) MODELG
      IF(MODELG.EQ.1) THEN
         KNAMEQ='eqdata'
      ELSE
         KNAMEQ='data1'
      ENDIF
      CALL EQLOAD(MODELG,KNAMEQ,IERR)
      IF(IERR.EQ.1) GOTO 1
      CALL EQSETP
      CALL EQPSIC(NRMAX,NTHMAX,NSUMAX)
C
    2 WRITE(6,*) '# INPUT PSIN'
      READ(5,*,ERR=2,END=9000) PSIN
      IF(PSIN.LT.0.D0) GOTO 9000
      CALL GETPP(PSIN,PP)
      CALL GETQP(PSIN,QP)
      CALL GETRMN(PSIN,RRMINL)
      CALL GETRMX(PSIN,RRMAXL)
      WRITE(6,'(A,1P4E12.4)') 'PP,QP,RRMIN,RRMAX =',PP,QP,RRMINL,RRMAXL
      GOTO 2
C
 9000 CALL GSCLOS
      STOP
      END
