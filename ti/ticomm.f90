    IMPLICIT NONE

    DEALLOCATE(NSA_NEQ,NV_NEQ)
    DEALLOCATE(MAT_LOCAL,VEC_LOCAL,VEC_GLOBAL,SOL_PREV,SOL_NEW)

    RETURN
  END SUBROUTINE deallocate_neqmax

